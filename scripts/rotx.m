function R = rotx(angle)
  ct = cos(angle);
  st = sin(angle);
  R(1,1) = 1.0;  R(1,2) = 0.0;  R(1,3) = 0.0;  R(1,4) = 0.0;
  R(2,1) = 0.0;  R(2,2) = ct;    R(2,3) = -st;   R(2,4) = 0.0;
  R(3,1) = 0.0;  R(3,2) =  st;   R(3,3) = ct;    R(3,4) = 0.0;
  R(4,1) = 0.0;  R(4,2) = 0.0;  R(4,3) = 0.0;  R(4,4) = 1.0;
end;
